let contador = 1;
let contador2 = 1;
//Estrutura while
console.log('While');
while(contador <=10){
    console.log(contador);
    contador++;
}
console.log('Do While');
// Estrutura do while
do{
    console.log(contador2);
    contador2++;
}while(contador2 <=10);

// Estrutura For
console.log('For');
for (let i = 1; i<=10; i++){
    console.log(i);
}